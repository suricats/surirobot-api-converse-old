'use strict';

exports.checkRequest = function(args, res, toReturn) {
	if (!args.audio || !args.audio || !args.audio.value || !args.audio.value.buffer) {
		res.statusCode = 400;
        res.statusMessage = "Converse API: Bad request: missing audio field";
        console.log(res.statusMessage);
        res.end(JSON.stringify(toReturn[Object.keys(toReturn)[0]] || {}, null, 2));
		return false;
	}
	if (!args.language || !args.language.value) {
		res.statusCode = 400;
        res.statusMessage = "Converse API: Bad request: missing language field";
        console.log(res.statusMessage);
        res.end(JSON.stringify(toReturn[Object.keys(toReturn)[0]] || {}, null, 2));
		return false;
	}

	return true;
};

exports.checkSTTresult = function(text, res, toReturn) {
	if (!text || !text.match(/[a-z0-9]/i)) {
		res.statusCode = 503;
        res.statusMessage = "Converse API: Input text extracted with STT is empty";
        console.log(res.statusMessage);
        res.end(JSON.stringify(toReturn[Object.keys(toReturn)[0]] || {}, null, 2));
		return false;
	}
	return true;
};

exports.checkSTTanswer = function(stt_res, res, toReturn) {
	if (!stt_res || !stt_res.body || !stt_res.body.data || !stt_res.body.data.text) {
		res.statusCode = 503;
        res.statusMessage = "Converse API: Input text extracted with STT cannot be found in STT response";
		if (stt_res.res.req.res.body) res.statusMessage += ": STT returned code " + stt_res.res.req.res.body.code + " and status message: " + stt_res.res.req.res.body.msg;
        console.log(res.statusMessage);
		console.log(stt_res);
        res.end(JSON.stringify(toReturn[Object.keys(toReturn)[0]] || {}, null, 2));
		return false;
	}
	return true;
};

